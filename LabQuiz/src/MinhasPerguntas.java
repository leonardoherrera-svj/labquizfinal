    private void btnExcluirPerguntaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirPerguntaActionPerformed
int linhaSelecionada = tblPerguntas.getSelectedRow();

if (linhaSelecionada == -1) {
    javax.swing.JOptionPane.showMessageDialog(this, "Selecione uma pergunta para excluir.");
    return;
}

int confirmacao = javax.swing.JOptionPane.showConfirmDialog(
        this,
        "Tem certeza que deseja excluir esta pergunta?",
        "Confirmar exclusão",
        javax.swing.JOptionPane.YES_NO_OPTION
);

if (confirmacao == javax.swing.JOptionPane.YES_OPTION) {
    try {
        int id = Integer.parseInt(tblPerguntas.getValueAt(linhaSelecionada, 0).toString());

        DAO dao = new DAO();
        dao.excluirPergunta(id);

        javax.swing.JOptionPane.showMessageDialog(this, "Pergunta excluída com sucesso!");

        carregarPerguntas();

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Erro ao excluir pergunta.");
        e.printStackTrace();
    }
}        // TODO add your handling code here:
    }//GEN-LAST:event_btnExcluirPerguntaActionPerformed

