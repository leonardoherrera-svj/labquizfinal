    private void carregarPerguntas() {
    try {
        DAO dao = new DAO();
        java.util.ArrayList<Pergunta> perguntas = dao.listarPerguntas();

        javax.swing.table.DefaultTableModel modelo =
                (javax.swing.table.DefaultTableModel) tblPerguntas.getModel();

        modelo.setRowCount(0);

        for (Pergunta pergunta : perguntas) {
            modelo.addRow(new Object[]{
                pergunta.getId(),
                pergunta.getEnunciado(),
                pergunta.getNivel(),
                pergunta.getRespostaCorreta()
            });
        }

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Erro ao carregar perguntas.");
        e.printStackTrace();
    }
}
    
