    private void carregarRanking() {
    try {
        DAO dao = new DAO();
        java.util.ArrayList<Object[]> ranking = dao.listarRanking();

        javax.swing.table.DefaultTableModel modelo =
                (javax.swing.table.DefaultTableModel) tblRanking.getModel();

        modelo.setRowCount(0);

        int posicao = 1;

        for (Object[] linha : ranking) {
            modelo.addRow(new Object[]{
                posicao,
                linha[0],
                linha[1],
                linha[2],
                linha[3]
            });

            posicao++;
        }

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Erro ao carregar ranking.");
        e.printStackTrace();
    }
}

